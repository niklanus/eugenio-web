				<h2 class="main-title"><span class="main">Microcirugía</span></h2>
				
				<p>La Microcirugía es el campo quirúrgico que emplea técnicas de reparación de los vasos sanguíneos y de los nervios mediante magnificación (con lupas o microscopio). Por microcirugía entendemos “la rama de la cirugía que se sirve de la magnificación del campo operatorio por encima de la agudeza visual normal” Es decir, allá donde el ojo humano no llega, llega la microcirugía y con ella el desarrollo constante de nuevas técnicas y procedimientos para trabajar con material microscópico.</p>
				
